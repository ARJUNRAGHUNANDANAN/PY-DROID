''' Python program to find area of a Circle given its Radius
By - Arjun Raghunandanan 
'''

RAD=float(input("Enter the radius of the circle"))

'''  Accepted the measure of radius as RAD  '''

area=3.14*RAD**2
'''  Formula for finding area of circle is (3.14*(RADIUS**2))  '''

'''  printing the value of area  '''
print("area of circle is :",area)
